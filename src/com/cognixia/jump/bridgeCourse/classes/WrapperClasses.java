package com.cognixia.jump.bridgeCourse.classes;

public class WrapperClasses {

		public static void main(String[] args) {
				// Wrapper Classes
				int a = 0;
				char b = 'b';
				boolean c = true;
				double e = 0;
				float f = 0;
				long g = 0;
				short h = 0;

				// There is a corresponding wrapper class for each primitive, in order to access
				// the object methods on each one

				Integer A = a;
				Character B = b;
				Boolean C = c;
				Double E = e;
				Float F = f;
				Long G = g;
				Short H = h;

				// can use assignment operator on wrapper classes, and can now access methods
				// using dot notiation on the Class
		}

}
